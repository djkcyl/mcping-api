//! 版本名 → 协议号映射,给「按某版本 ping」用。
//!
//! 关键事实(已逐源码核实):从 1.7 到 26.1,现代 SLP 握手的字节布局**完全一致** —— 换版本只换
//! 握手里那一个 `protocol` VarInt,其余请求方式、包结构一字不变。所以「以某版本身份 ping」= 把对应
//! 协议号填进 [`PingOptions::protocol_version`](super::PingOptions),没有任何随版本变化的额外字节。
//! (pre-1.7 是另一套 0xFE legacy 形态,见 [`legacy`](super::legacy);此处只管 1.7+ 的协议号。)
//!
//! 服务器**不会**因协议号不同而改变 status 响应内容(协议号校验只发生在 login 阶段),所以这里主要服务
//! 于两个场景:① 严格服务器要求真实协议号才肯回应;② 触发 ViaVersion 之类按客户端协议改写 `version`
//! 的行为(见 [`ping_resolved`](super::ping_resolved))。

/// 最新已知正式版协议号(26.1)。仅作「我们认得的最高版本」参考用,**不**拿它去回探代理后端。
pub const LATEST: i32 = 775;

