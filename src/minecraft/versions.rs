/// 客户端版本**翻译**到后端。所以回探只需用一个它**认得**的协议号即可(认得就会翻译并转发后端真实 status);
/// 用最新版反而最危险——比 ViaProxy 构建版本更新的协议号没被注册,会和 -1 一样被踢。老一些的稳定版几乎被所有
