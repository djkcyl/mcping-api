/// 把版本名解析成协议号。先精确匹配;裸串数字本身就是协议号时也接受(如 `"47"`)。匹配不到给 `None`。
pub fn protocol_for(version: &str) -> Option<i32> {
    let v = version.trim();
    if let Some((_, p)) = TABLE.iter().find(|(name, _)| name.eq_ignore_ascii_case(v)) {
        return Some(*p);
    }
    // 直接给协议号(纯数字)也认。
    v.parse::<i32>().ok()
}
